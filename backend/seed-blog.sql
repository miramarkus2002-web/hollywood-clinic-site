## How Many Sessions Do You Need?

The standard protocol is **2–4 sessions, spaced 3–4 weeks apart**. Hair biology takes time, so:

- **Weeks 3–4:** shedding begins to slow
- **Weeks 8–12:** new growth becomes visible
- **Months 4–6:** density and quality improvements peak

After the initial course, most patients benefit from maintenance sessions every 6–12 months to preserve and extend results.

